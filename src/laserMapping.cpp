/**
 * @brief 设置点云匹配估计的初值位姿(从里程计世界坐标系转到地图世界坐标系)
 *
 * @param 无
 * @return 无
    * @retval
 */
// set initial guess
void transformAssociateToMap()
{
	q_w_curr = q_wmap_wodom * q_wodom_curr;
	t_w_curr = q_wmap_wodom * t_wodom_curr + t_wmap_wodom;
}



/**
 * @brief 计算优化后的地图世界系到里程计世界系的位姿变换,用于计算下一帧的优化初值
 *
 * @param 无
 * @return 无
    * @retval
 */
void transformUpdate()
{
	q_wmap_wodom = q_w_curr * q_wodom_curr.inverse();
	t_wmap_wodom = t_w_curr - q_wmap_wodom * t_wodom_curr;
}



/**
 * @brief 把当前帧点云转换到地图世界系下
 *
 * @param pi    输入特征点(当前帧坐标系下)
 * @param po    输出特征点(世界坐标系下)
 * @return 无
    * @retval
 */
void pointAssociateToMap(PointType const *const pi, PointType *const po)
{
	Eigen::Vector3d point_curr(pi->x, pi->y, pi->z);
	Eigen::Vector3d point_w = q_w_curr * point_curr + t_w_curr;
	po->x = point_w.x();
	po->y = point_w.y();
	po->z = point_w.z();
	po->intensity = pi->intensity;
	//po->intensity = 1.0;
}


/**
 * @brief 把地图世界系下点云转换到当前帧下
 *
 * @param pi    输入特征点(世界坐标系下)
 * @param po    输出特征点(当前帧坐标系下)
 * @return 无
    * @retval
 */
void pointAssociateTobeMapped(PointType const *const pi, PointType *const po)
{
	Eigen::Vector3d point_w(pi->x, pi->y, pi->z);
	Eigen::Vector3d point_curr = q_w_curr.inverse() * (point_w - t_w_curr);
	po->x = point_curr.x();
	po->y = point_curr.y();
	po->z = point_curr.z();
	po->intensity = pi->intensity;
}



